import math
import random

while True:
    print("Выберите операцию:")
    print("+ для сложения")
    print("- для вычитания")
    print("* для умножения")
    print("/ для деления")
    print("** для возведения в степень")
    print("mod для вычисления модуля")
    print("rd для генерации случайного числа")
    print("! для вычисления факториала")
    print("acos для вычисления арккосинуса")

    choice = input("Введите операцию: ")
    # Обработка операции сложения
    if choice == "+":
        a = float(input("Введите первое слагаемое: "))
        b = float(input("Введите второе слагаемое: "))
        print("Результат:", a + b)

    # Обработка операции вычитания
    if choice == "-":
        a = float(input("Введите уменьшаемое: "))
        b = float(input("Введите вычитаемое: "))
        print("Результат:", a - b)

    # Обработка операции умножения
    if choice == "*":
        a = float(input("Введите первый множитель: "))
        b = float(input("Введите второй множитель: "))
        print("Результат:", a * b)

    # Обработка операции деления
    if choice == "/":
        a = float(input("Введите делимое: "))
        b = float(input("Введите делитель: "))
        if b == 0:
            print("Ошибка: деление на ноль")
        else:
            print("Результат:", a / b)

    # Обработка операции возведения в степень
    if choice == "**":
        a = float(input("Введите число: "))
        b = float(input("Введите степень: "))
        print("Результат:", a ** b)

    # Обработка операции вычисления модуля
    if choice == "mod":
        a = float(input("Введите число: "))
        print("Результат:", abs(a))

    # Обработка операции генерации случайного числа
    if choice == "rd":
        a = float(input("Введите минимальное значение: "))
        b = float(input("Введите максимальное значение: "))
        print("Результат:", random.uniform(a, b))

    # Обработка операции вычисления факториала
    if choice == "!":
        n = int(input("Введите число: "))
        print("Результат:", math.factorial(n))

    # Обработка операции вычисления арккосинуса
    if choice == "acos":
        x = float(input("Введите число: "))
        print("Результат:", math.acos(x))

    else:
        print("Ошибка: неподдерживаемая операция")